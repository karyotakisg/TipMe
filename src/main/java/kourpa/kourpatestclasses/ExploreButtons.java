public class ExploreButtons {

	public static String numbers(int n) {
		if (n == 0) {
			return "Science";
		} else if (n == 1) {
			return "Sports";
		} else if (n == 2) {
			return "Music";
		} else if (n == 3) {
			return "Fashion";
		} else if (n == 4) {
			return "Travel";
		} else  {
			return "Fitness & Lifestyle";
		}

	}


}






